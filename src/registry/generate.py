ROOT_DIR = Path(__file__).resolve().parents[2]
MARKDOWN_HEADER = "<!-- Generated by src.registry.generate. Do not edit by hand. -->"
CODEGEN_HEADER = "/* Generated by src.registry.generate. Do not edit by hand. */"


def _json_text(payload: dict[str, object]) -> str:
    return json.dumps(payload, indent=2, sort_keys=False) + "\n"


def _ts_value(value: object, *, indent: int = 0) -> str:
    spacer = " " * indent
    if isinstance(value, str):
        return json.dumps(value)
    if isinstance(value, bool):
        return "true" if value else "false"
    if value is None:
        return "null"
    if isinstance(value, (int, float)):
        return str(value)
    if isinstance(value, list):
        if not value:
            return "[]"
        inner = ",\n".join(f"{' ' * (indent + 2)}{_ts_value(item, indent=indent + 2)}" for item in value)
        return "[\n" + inner + f"\n{spacer}]"
    if isinstance(value, tuple):
        return _ts_value(list(value), indent=indent)
    if isinstance(value, dict):
        if not value:
            return "{}"
        lines = []
        for key, item in value.items():
            lines.append(f"{' ' * (indent + 2)}{key}: {_ts_value(item, indent=indent + 2)}")
        return "{\n" + ",\n".join(lines) + f"\n{spacer}}}"
    raise TypeError(f"Unsupported TS literal value: {type(value)!r}")


def _ts_string_map(values: dict[str, str]) -> str:
    items = ",\n".join(f'  {json.dumps(key)}: {json.dumps(value)}' for key, value in values.items())
    return "{\n" + items + "\n}"


def _generated_league_ts() -> str:
    entries = ordered_league_entries()
    all_leagues = [entry.code for entry in entries]
    alias_map: dict[str, str] = {}
    runtime: dict[str, object] = {}
    for entry in entries:
        alias_map[entry.code] = entry.code
        alias_map[entry.slug.upper()] = entry.code
        for alias in entry.aliases:
            alias_map[alias.upper()] = entry.code
        runtime[entry.code] = {
            "code": entry.code,
            "slug": entry.slug,
            "displayLabel": entry.display_label,
            "defaultConfigPath": entry.default_config_path,
            "configEnvVar": entry.config_env_var,
            "projectName": entry.project_name,
            "dbPath": entry.db_path,
            "dbEnvVar": entry.db_env_var,
            "championshipName": entry.championship_name,
            "championshipProbabilityKey": entry.championship_probability_key,
            "uncertaintyPolicyName": entry.uncertainty_policy_name,
            "aliases": list(entry.aliases),
        }
    return "\n".join(
        [
            f"{CODEGEN_HEADER}",
            "",
            "export const ALL_LEAGUES = " + _ts_value(all_leagues) + " as const;",
            "export type LeagueCode = typeof ALL_LEAGUES[number];",
            "",
            "export type LeagueRuntimeEntry = {",
            "  code: LeagueCode;",
            "  slug: string;",
            "  displayLabel: string;",
            "  defaultConfigPath: string;",
            "  configEnvVar: string;",
            "  projectName: string;",
            "  dbPath: string;",
            "  dbEnvVar: string;",
            "  championshipName: string;",
            "  championshipProbabilityKey: string;",
            "  uncertaintyPolicyName: string;",
            "  aliases: readonly string[];",
            "};",
            "",
            "export const LEAGUE_ALIASES: Record<string, LeagueCode> = " + _ts_string_map(alias_map) + ";",
            "export const LEAGUE_RUNTIME: Record<LeagueCode, LeagueRuntimeEntry> = "
            + _ts_value(runtime)
            + ";",
            "",
        ]
    ) + "\n"


def _generated_model_ts() -> str:
    payload = model_manifest_payload()
    model_registry = payload["models"]
    return "\n".join(
        [
            f"{CODEGEN_HEADER}",
            "",
            "export const TRAINABLE_MODELS = " + _ts_value(payload["trainable_models"]) + " as const;",
            "export type TrainableModel = typeof TRAINABLE_MODELS[number];",
            "",
            "export const MODEL_ALIASES: Record<string, string> = " + _ts_value(payload["aliases"]) + ";",
            "export const LEGACY_MODEL_KEYS: Record<string, readonly string[]> = "
            + _ts_value(payload["legacy_model_keys"])
            + ";",
            "export const MODEL_REPORT_ORDER = " + _ts_value(payload["prediction_report_order"]) + " as const;",
            "export const MODEL_DISPLAY_LABELS: Record<string, string> = " + _ts_value(payload["display_labels"]) + ";",
            "export const MODEL_REGISTRY = " + _ts_value(model_registry) + ";",
            "",
        ]
    ) + "\n"


def _generated_dashboard_routes_ts() -> str:
    routes = []
    for route in dashboard_routes():
        routes.append(
            {
                "key": route.key,
                "summary": route.summary,
                "modulePath": route.module_path,
                "apiPath": route.api_path,
                "pagePath": route.page_path,
                "stagingFileName": route.staging_file_name,
                "payloadContract": route.payload_contract,
                "includeInStaging": route.include_in_staging,
                "public": route.public,
                "supportsExperiments": route.supports_experiments,
            }
        )
    staging_keys = [route.key for route in staging_dashboard_routes()]
    return "\n".join(
        [
            f"{CODEGEN_HEADER}",
            "",
            "export const DASHBOARD_ROUTE_KEYS = " + _ts_value([route["key"] for route in routes]) + " as const;",
            "export type DashboardRouteKey = typeof DASHBOARD_ROUTE_KEYS[number];",
            "",
            "export type DashboardRouteEntry = {",
            "  key: DashboardRouteKey;",
            "  summary: string;",
            "  modulePath: string;",
            "  apiPath: string;",
            "  pagePath: string | null;",
            "  stagingFileName: string;",
            "  payloadContract: DashboardRouteKey;",
            "  includeInStaging: boolean;",
            "  public: boolean;",
            "  supportsExperiments: boolean;",
            "};",
            "",
            "export const DASHBOARD_ROUTES: readonly DashboardRouteEntry[] = " + _ts_value(routes) + ";",
            "export const DASHBOARD_STAGING_ROUTE_KEYS = " + _ts_value(staging_keys) + " as const;",
            "export const DASHBOARD_STAGING_ROUTES = DASHBOARD_ROUTES.filter((route) => route.includeInStaging);",
            "",
        ]
    ) + "\n"


def _architecture_doc() -> str:
    rows = [
        "| Path | Role | Public Entrypoints |",
        "| --- | --- | --- |",
    ]
    for entry in subsystem_docs():
        rows.append(
            f"| `{entry.path}` | {entry.summary} | {', '.join(f'`{item}`' for item in entry.public_entrypoints)} |"
        )
    return "\n".join(
        [
            MARKDOWN_HEADER,
            "",
            "# Generated Architecture Map",
            "",
            "This reference is generated from the code registries. Update the registry modules, then regenerate docs.",
            "",
            *rows,
            "",
        ]
    )


def _command_doc() -> str:
    sections = [MARKDOWN_HEADER, "", "# Generated Command Reference", ""]
    for entry in command_registry():
        sections.extend([f"## `{entry.name}`", "", entry.summary, ""])
        sections.append(f"- Default config: `{command_manifest_payload()['default_config_path']}`")
        if entry.arguments:
            sections.append("- Options:")
            for argument in entry.arguments:
                label = ", ".join(f"`{flag}`" for flag in argument.flags)
                hint = f" `{argument.doc_metavar}`" if argument.doc_metavar else ""
                sections.append(f"  - {label}{hint}: {argument.argparse_kwargs.get('help', '')}")
        if entry.examples:
            sections.append("- Examples:")
            for example in entry.examples:
                sections.append(f"  - `{example}`")
        sections.append("")
    return "\n".join(sections).rstrip() + "\n"


def _dashboard_route_doc() -> str:
    rows = [
        "| Key | API Path | Staging File | Page | Experiments |",
        "| --- | --- | --- | --- | --- |",
    ]
    for route in dashboard_routes():
        rows.append(
            f"| `{route.key}` | `{route.api_path}` | `{route.staging_file_name}` | "
            f"{'`' + route.page_path + '`' if route.page_path else 'n/a'} | "
            f"{'yes' if route.supports_experiments else 'no'} |"
        )
    return "\n".join(
        [
            MARKDOWN_HEADER,
            "",
            "# Generated Dashboard Route Reference",
            "",
            "All dashboard API and staging routes are derived from the code-first dashboard route registry.",
            "",
            *rows,
            "",
        ]
    )


