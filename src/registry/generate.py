def _manifest_doc() -> str:
    return "\n".join(
        [
            MARKDOWN_HEADER,
            "",
            "# Generated Manifest Inventory",
            "",
            "- `configs/generated/league_manifest.json`: canonical league metadata for Python, web, and docs consumers.",
            "- `configs/generated/model_manifest.json`: canonical model labels, aliases, ordering, and trainable-model set.",
            "- `configs/generated/command_manifest.json`: canonical CLI command metadata and examples.",
            "- `configs/generated/dashboard_route_manifest.json`: canonical dashboard API/staging route inventory.",
            "- `web/lib/generated/*.ts`: generated TypeScript registry surfaces derived from the same Python source registries.",
            "",
        ]
    )


def _generated_readme(entry_path: str, title: str, summary: str, public_entrypoints: tuple[str, ...], notes: tuple[str, ...]) -> str:
    lines = [
        MARKDOWN_HEADER,
        "",
        f"# {title}",
        "",
        summary,
        "",
        "Public entrypoints:",
    ]
    for item in public_entrypoints:
        lines.append(f"- `{item}`")
    if notes:
        lines.extend(["", "Notes:"])
        for note in notes:
            lines.append(f"- {note}")
    lines.extend(
        [
            "",
            f"This README is generated from the subsystem registry entry for `{entry_path}`.",
            "",
        ]
    )
    return "\n".join(lines)


def _write_or_check(path: Path, content: str, *, check: bool, changed_paths: list[Path]) -> None:
    existing = path.read_text() if path.exists() else None
    if existing == content:
        return
    changed_paths.append(path)
    if check:
        return
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(content)


def generate_all(*, root_dir: Path = ROOT_DIR, check: bool = False) -> list[Path]:
    """Generate or validate all registry-backed artifacts."""

    changed_paths: list[Path] = []
    manifest_dir = root_dir / "configs" / "generated"
    docs_dir = root_dir / "docs" / "generated"
    web_generated_dir = root_dir / "web" / "lib" / "generated"

    outputs: dict[Path, str] = {
        manifest_dir / "league_manifest.json": _json_text(league_manifest_payload()),
        manifest_dir / "model_manifest.json": _json_text(model_manifest_payload()),
        manifest_dir / "command_manifest.json": _json_text(command_manifest_payload()),
        manifest_dir / "dashboard_route_manifest.json": _json_text(dashboard_route_manifest_payload()),
        docs_dir / "architecture.md": _architecture_doc(),
        docs_dir / "commands.md": _command_doc(),
        docs_dir / "dashboard-routes.md": _dashboard_route_doc(),
        docs_dir / "extensions.md": _extensions_doc(),
        docs_dir / "manifests.md": _manifest_doc(),
        web_generated_dir / "league-registry.ts": _generated_league_ts(),
        web_generated_dir / "model-manifest.ts": _generated_model_ts(),
        web_generated_dir / "dashboard-routes.ts": _generated_dashboard_routes_ts(),
    }

    for entry in subsystem_docs():
        if entry.generate_readme and entry.readme_path:
            outputs[root_dir / entry.readme_path] = _generated_readme(
                entry.path,
                entry.title,
                entry.summary,
                entry.public_entrypoints,
                entry.notes,
            )

    for path, content in outputs.items():
        _write_or_check(path, content, check=check, changed_paths=changed_paths)

    return changed_paths


def main() -> None:
    """Generate or check deterministic registry-backed artifacts."""

    parser = argparse.ArgumentParser(description="Generate registry-backed manifests, docs, and boilerplate READMEs.")
    parser.add_argument(
        "--check",
        action="store_true",
        help="Fail if generated artifacts are stale instead of rewriting them.",
    )
    args = parser.parse_args()

    changed_paths = generate_all(check=bool(args.check))
    if args.check and changed_paths:
        for path in changed_paths:
            print(path.relative_to(ROOT_DIR))
        raise SystemExit(1)


if __name__ == "__main__":
    main()
